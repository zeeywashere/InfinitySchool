///////////////// Notas e Pesos /////////////////


let n1 = 1.4, n2 = 9.3, n3 = 6.5;
let p1 = 2, p2 = 3, p3 = 5;

///////////////// Calculo da Média /////////////////


let media = (n1 * p1 + n2 * p2 + n3 * p3) / (p1 + p2 + p3);


///////////////// Mostrar a Média /////////////////


console.log('A sua média é:', media);
